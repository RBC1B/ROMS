package uk.org.rbc1b.roms.db;

/**
 * The basic person identifier.
 * <p>
 * This can be extended to volunteers, users, and non RBC-overseers.
 * @author oliver.elder.esq
 */
public class Person extends DefaultUpdateAuditable {
    private static final long serialVersionUID = 8345111360587695632L;
    private Integer personId;
    private java.sql.Date birthDate;
    private Integer congregationId;
    private String forename;
    private String middleName;
    private String surname;
    private Address address;
    private String telephone;
    private String mobile;
    private String workPhone;
    private String email;
    private String comments;

    /**
     * @return the person display name - "forename surname"
     */
    public String formatDisplayName() {
        return forename + " " + surname;
    }

    public Address getAddress() {
        return address;
    }

    public void setAddress(Address address) {
        this.address = address;
    }

    public java.sql.Date getBirthDate() {
        return birthDate;
    }

    public void setBirthDate(java.sql.Date birthDate) {
        this.birthDate = birthDate;
    }

    public String getComments() {
        return comments;
    }

    public void setComments(String comments) {
        this.comments = comments;
    }

    public Integer getCongregationId() {
        return congregationId;
    }

    public void setCongregationId(Integer congregationId) {
        this.congregationId = congregationId;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getForename() {
        return forename;
    }

    public void setForename(String forename) {
        this.forename = forename;
    }

    public String getMiddleName() {
        return middleName;
    }

    public void setMiddleName(String middleName) {
        this.middleName = middleName;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public Integer getPersonId() {
        return personId;
    }

    public void setPersonId(Integer personId) {
        this.personId = personId;
    }

    public String getSurname() {
        return surname;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

    public String getTelephone() {
        return telephone;
    }

    public void setTelephone(String telephone) {
        this.telephone = telephone;
    }

    public String getWorkPhone() {
        return workPhone;
    }

    public void setWorkPhone(String workPhone) {
        this.workPhone = workPhone;
    }

    @Override
    public String toString() {
        return "Person{" + "personId=" + personId + '}';
    }
}
